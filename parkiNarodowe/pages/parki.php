        <h1>Parki narodowe w Polsce</h1>
        <h2>Witam na stronie poświeconej Polskim Parkom Narodowym</h2><br>
        Definicja Parku Narodowego:
    </div  >
    <strong>Park narodowy</strong> obejmuje obszar wyróżniający się szczególnymi wartościami przyrodniczymi, naukowymi, 
    społecznymi, kulturowymi i edukacyjnymi, o powierzchni nie mniejszej niż 1000 ha.
    
    Parki narodowe tworzy się, aby zachować różnorodność biologiczną, przyrodę nieożywioną oraz walory
    krajobrazowe, a także przywrócić właściwy stan przyrody lub odtworzyć zniekształcone siedliska przyrodnicze. 
    Parki narodowe to państwowe osoby prawne, nadzorowane przez Ministra Środowiska.
    Aby zapobiec negatywnemu oddziaływaniu na przyrodę, zwiedzanie parków narodowych podlega szczególnym ograniczeniom, 
    wynikającym z przepisów prawa. Ruch turystyczny odbywa się wyłącznie w wyznaczonych obszarach i na określonych warunkach. 
    Za wstęp do niektórych parków pobierana jest opłata, z której park pokrywa część kosztów funkcjonowania.
